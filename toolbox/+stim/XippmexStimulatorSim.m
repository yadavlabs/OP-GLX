    methods

        function obj = XippmexStimulatorSim(opts)
            %% constructor
            arguments
                opts.channelMap dictionary %dictionary where keys correspond to channels labels of leads and values correspond to channels of ripple
                opts.frontEndType = "Pico"
            end

            obj.stimulationParameters = obj.defaultStimulationParameters;
            obj.parameterNameMap = dictionary(string(fieldnames(obj.stimulationParameters)), obj.parameterDisplayNames);

            if isfield(opts, "channelMap")
               obj.channelMap = opts.channelMap;
               obj.applyMap = true;
            end
            if isfield(opts, "frontEndType")
                switch opts.frontEndType
                    case "Macro"
                        obj.ampResValList = stim.constants.AMPRESVALS_MACRO;
                        obj.maxStepsList = stim.constants.MAXSTEPS_MACRO;

                    case "Pico"
                        obj.ampResValList = stim.constants.AMPRESVALS_PICO;
                        obj.maxStepsList = stim.constants.MAXSTEPS_PICO;

                end
            end
            
            %obj.stimulationCommand = stim.generateSpatiotemporalPattern(obj.defaultStimulationParameters);
            
            % obj.stimTimer = timer('StartDelay', obj.stimulationParameters.train_length, ...
            %     'TimerFcn', @(~,~)obj.handleStimulusDelivered, ...
            %     'ExecutionMode','singleShot', 'Name', 'Stim Timer');


        end

        function initialize(obj, opts)
            %% initializes xippmex connection
            arguments
                obj stim.XippmexStimulatorSim
                opts.mode
                opts.address
            end

            % check if already initialized
            if obj.isInitialized
                obj.StatusMessage = "Stimulator already initialized.";
                return;
            end
            
            % set optional inputs
            if isfield(opts, "mode")
                obj.mode = opts.mode;
            end
            if isfield(opts, "address")
                obj.address = opts.address;
            end
            
            % enable xippmex 
            switch obj.mode
                case "TCP"
                    status = 1;%xippmex('tcp');
                otherwise
                    status = 1;%xippmex;
            end
            
            % return if initialization failed
            if status ~= 1
                obj.StatusMessage = "Xippmex did not initialize.";
                notify(obj, "InitializationError")
                return;
            end

            % check for stimulation channels on headstage
            stimChans = 1:32;%xippmex('elec','stim');
            if isempty(stimChans)
                obj.StatusMessage = "Warning: No stimulation hardware detected.";
                notify(obj, "InitializationError")
                return;
            end
            
            
            
            obj.stimulationChannels = stimChans; 
            if obj.applyMap
                obj.selectedChannels = stimChans(ismember(stimChans, values(obj.channelMap)));
            else
                obj.selectedChannels = stimChans;
            end
            obj.stimTimer = timer('StartDelay', obj.stimulationParameters.train_length, ...
                'TimerFcn', @(~,~)obj.handleStimulusDelivered, ...
                'ExecutionMode','singleShot', 'Name', 'Stim Timer');
            obj.isInitialized = true; 
            obj.StatusMessage = "Stimulator initialized successfully.";
            notify(obj, "InitializationSuccess")
            
            generateClearCmd(obj);
            enableRecording(obj);
            updateAmplitudeResolution(obj, obj.stimulationParameters.step)

        end

        function connected = isConnected(obj)
            connected = obj.isInitialized;
        end

        function updateParameters(obj, parameters)
            if ~obj.isInitialized
                obj.StatusMessage = "Xippmex not initialized.";
                return;
            end
            % parameters = struct("vibration_level", 100)
            % parameters = struct("vibration_level", 100, "vibration_length", 2000)
            for field = string(fieldnames(parameters))'
                new_param = parameters.(field);
                if strcmp(field, 'anode') || strcmp(field, 'cathode') && obj.applyMap
                    obj.stimulationParameters.(field) = obj.channelMap(new_param);
                else
                    obj.stimulationParameters.(field) = new_param;
                end
                if ~strcmp(field, 'amplitude')
                    obj.StatusMessage = obj.parameterNameMap(field) + " set to: " + num2str(new_param);
                end
            end
            obj.stimTimer.StartDelay = obj.stimulationParameters.train_length;
            %obj.stimulationCommand = stim.generateSpatiotemporalPattern(obj.stimulationParameters);
            [obj.stimulationCommand, amp_info] = stim.generateStimulationCommand(obj.stimulationParameters);
            %obj.stimulationCommand;
            if ~amp_info.validInput
                obj.stimulationParameters.amplitude = amp_info.ampValid;
                
                %obj.stimulationParameters.amplitude_anode] = deal(obj.stimulationParameters.amplitude);
                obj.StatusMessage = sprintf('Amplitude corrected to %d uA', amp_info.ampValid);
                notify(obj, "AmplitudeCorrected");
            end
            if ~obj.stimulationParameters.sequential
                obj.StatusMessage = sprintf('Cathode amplitude set to %d uA', amp_info.ampCathode);
                obj.StatusMessage = sprintf('Anode amplitude set to %d uA', amp_info.ampAnode);
                obj.stimulationParameters.amplitude_cathode = amp_info.ampCathode;
                obj.stimulationParameters.amplitude_anode = amp_info.ampAnode;
                notify(obj, "AmplitudeDistributed");
                
            else
                [obj.stimulationParameters.amplitude_cathode, obj.stimulationParameters.amplitude_anode] = deal(obj.stimulationParameters.amplitude);
            end
            obj.StatusMessage = "Parameters updated.";
            %obj.StatusMessage = sprintf('Cathodes: %s', strjoin(string(obj.stimulationParameters.cathode), ', '));
            %obj.StatusMessage = sprintf('Anodes: %s', strjoin(string(obj.stimulationParameters.anode), ', '));

        end

