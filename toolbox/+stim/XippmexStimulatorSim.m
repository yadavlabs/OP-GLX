        function deliverStimulus(obj)
            %% deliver stimuation at with current stimulation command
            if ~obj.isInitialized
                notify(obj, "StimulationError")
                obj.StatusMessage = "Xippmex not initialized.";
                return;
            end
            % disp('here')
            obj.StatusMessage = "Delivering stimulation...";
            for i = 1:length(obj.stimulationCommand)
                %xippmex('stimseq', obj.stimulationCommand{i})
                obj.StatusMessage = "Queue" + num2str(i);
            end
            start(obj.stimTimer)

        end



        function stopStimulus(obj)
            if ~obj.isInitialized
                return;
            end
            obj.StatusMessage = "Stimulation Stopped.";
            %writeline(obj.serObj, 'V0')
        end

        function deliverCMD(obj, cmd_cell)
        %% USE CAREFULLY
        % delivers stimulation stimseq command(s) in cmd_cell directly
        % only checks if XippmexStimulator object is initialized, not if
        % the contents in cmd_cell contain valid stimseq commands
        % Only has a try-catch statement that will trigger on stimseq errors
            if ~obj.isInitialized
                notify(obj, "StimulationError")
                obj.StatusMessage = "Xippmex not initialized.";
                return;
            end
            obj.StatusMessage = "Delivering stimulation...";
            try
                for i = 1:length(cmd_cell)
                    %xippmex('stimseq', cmd_cell{i})
                    obj.StatusMessage = "Queue" + num2str(i);
                end
                start(obj.stimTimer)

            catch ME
                notify(obj, "StimulationError")
                obj.StatusMessage = ME.message;
                assignin("base", "ME", ME)
                assignin("base", "cmd_cell", cmd_cell)
            end

        end

        function generateClearCmd(obj)
            if ~obj.isInitialized
                return;
            end
            for i = 1:length(obj.selectedChannels)
                obj.clearCMD(i).elec = obj.selectedChannels(i);
                obj.clearCMD(i).period = 20;
                obj.clearCMD(i).repeats = 1;
                obj.clearCMD(i).action = 'immed';
                obj.clearCMD(i).seq = struct('length', 3, 'ampl', 0, 'pol', 0, ...
                    'fs', 0, 'enable', 0, 'delay', 0, 'ampSelect', 0);
            end


        end

        function cleanup(obj)
            if ~obj.isInitialized
                obj.StatusMessage = "Xippmex not initialized";
                return;
            end

            % if xippmex('stim', 'enable') == 1
            %     xippmex('stim', 'enable', 0)
            %     pause(0.5)
            % end
            % xippmex('close')
            obj.isInitialized = false;
            obj.StatusMessage = "Xippmex closed.";

        end

        function delete(obj)
            
            cleanup(obj);
            if ~isempty(obj.stimTimer) && isvalid(obj.stimTimer)
                stop(obj.stimTimer)
                delete(obj.stimTimer)
                obj.stimTimer = [];
            end
        end

        function attatchListener(obj)
            addlistener(obj, "StatusMessage", "PostSet", @(src, event)stim.XippmexStimulatorSim.displayStatusMessage(@fprintf, src, event));
        end


        function updateAmplitudeResolution(obj, new_res_val)
            if ~obj.isInitialized
                obj.StatusMessage = "Xippmex not initialized.";
                return;
            end
            if new_res_val == obj.ampResVal
                obj.StatusMessage = "Amplitude resolution already applied.";
                return;
            end
            new_res = obj.ampResList(ismember(obj.ampResValList, new_res_val));
            if isempty(new_res) || ~isscalar(new_res)
                obj.StatusMessage = "Invalid amplitude resolution.";
                return;
            end
            

            % if xippmex('stim', 'enable') == 1
            %     xippmex('stim', 'enable', 0)
            %     pause(0.5)
            % end

            
            % xippmex('stim', 'res', obj.selectedChannels, new_res)
            % pause(0.2)
            % xippmex('stim', 'enable', 1)
            % pause(0.5)

            obj.ampRes = new_res;
            obj.ampResVal = new_res_val;
            obj.maxSteps = obj.maxStepsList(new_res);
            obj.ampMax = obj.ampResVal * obj.maxSteps;
            obj.ampMin = obj.ampResVal;
            obj.stimulationParameters.step = new_res_val;
            %obj.stimulationCommand = stim.generateSpatiotemporalPattern(obj.stimulationParameters);
            [obj.stimulationCommand, amp_info] = stim.generateStimulationCommand(obj.stimulationParameters, "fastSettle", obj.fastSettle);
            %obj.stimulationCommand;
            if ~amp_info.validInput
                obj.stimulationParameters.amplitude = amp_info.ampValid;
                
                %obj.stimulationParameters.amplitude_anode] = deal(obj.stimulationParameters.amplitude);
                obj.StatusMessage = sprintf('Amplitude corrected to %d uA', amp_info.ampValid);
                notify(obj, "AmplitudeCorrected");
            end
            if ~obj.stimulationParameters.sequential
                obj.StatusMessage = sprintf('Cathode amplitude set to %d uA', amp_info.ampCathode);
                obj.StatusMessage = sprintf('Anode amplitude set to %d uA', amp_info.ampAnode);
                obj.stimulationParameters.amplitude_cathode = amp_info.ampCathode;
                obj.stimulationParameters.amplitude_anode = amp_info.ampAnode;
                notify(obj, "AmplitudeDistributed");
                
            else
                [obj.stimulationParameters.amplitude_cathode, obj.stimulationParameters.amplitude_anode] = deal(obj.stimulationParameters.amplitude);
            end
            obj.StatusMessage = "Parameters updated.";
            obj.StatusMessage = "Amplitude resolution set to: " + obj.ampResVal + "uA";


        end


        function setGndRef(obj, chan)
            if ~obj.isInitialized
                obj.StatusMessage = "Xippmex not initialized.";
                return;
            end
            if obj.applyMap
                chan = obj.channelMap(chan);
            end
            %xippmex('ground', chan, 1);
            %xippmex('reference', chan, 1);
            obj.gndRefChannel = chan;
            obj.StatusMessage = "Ground/reference channel set: " + chan;
        end

        function removeGndRef(obj)
            if ~obj.isInitialized
                obj.StatusMessage = "Xippmex not initialized.";
                return;
            end
            if isempty(obj.gndRefChannel)
                obj.StatusMessage = "No ground/reference set";
                return;
            end
            %xippmex('ground', obj.gndRefChannel, 0);
            %xippmex('reference', obj.gndRefChannel, 0);
            
            obj.StatusMessage = "Ground/reference removed from channel " + obj.gndRefChannel;
            obj.gndRefChannel = [];
        end

        function setGnd(obj, chan)
            %% set selected channel 'chan' to ground only
            if ~obj.isInitialized
                obj.StatusMessage = "Xippmex not initialized.";
                return;
            end
            if ~obj.isInitialized
                obj.StatusMessage = "Xippmex not initialized.";
                return;
            end
            if obj.applyMap
                chan = obj.channelMap(chan);
            end

            %xippmex('ground', chan, 1);
            obj.gndChannel = chan;
            obj.StatusMessage = "Ground channel set: " + chan;
        end

        function removeGnd(obj)
            %% remove previously selected channel from ground only
            if ~obj.isInitialized
                obj.StatusMessage = "Xippmex not initialized.";
                return;
            end
            if isempty(obj.gndChannel)
                obj.StatusMessage = "No ground set";
                return;
            end
            %xippmex('ground', obj.gndChannel, 0);            
            obj.StatusMessage = "Ground removed from channel " + obj.gndChannel;
            obj.gndChannel = [];
        end

        function setRef(obj, chan)
