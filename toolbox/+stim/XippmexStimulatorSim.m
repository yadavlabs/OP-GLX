                obj.StatusMessage = "Xippmex not initialized.";
                return;
            end
            if isempty(obj.refChannel)
                obj.StatusMessage = "No reference set";
                return;
            end
            %xippmex('reference', obj.refChannel, 0);            
            obj.StatusMessage = "Ground removed from channel " + obj.refChannel;
            obj.refChannel = [];
        end

        function setRecordingParameters(obj, save_path)
            %% set recording parameters
            % currently just to specify save path. 
            % also resets autoincriment 
            if ~obj.isInitialized
                return;
            end
            if ~strcmp(obj.mode, "TCP")
                return;
            end
            if ~obj.recordingEnabled
                return;
            end
            disp('here')
            %xippmex('trial', obj.operatorAddress, save_path, 0, 0);
        end

        function startRecording(obj, save_file_path)
            if ~obj.recordingEnabled
                return;
            end
            
            %xippmex('trial', obj.operatorAddress, save_file_path, 0, 0, 1);
            %xippmex('trial', 'recording');
            obj.isRecording = true;
            obj.StatusMessage = "Recording started: " + string(save_file_path); 


        end

        function stopRecording(obj)
            if ~obj.isRecording
                return;
            end
            
            %xippmex('trial', obj.operatorAddress, 'stopped');
            try 
                %xippmex('trial', 'stopped');
                obj.StatusMessage = "Recording stopped.";
                obj.isRecording = false;
            catch ME
                if contains(ME.message, 'A file with this name already exists')
                    % when auto-incriment is set to off, stopping the
                    % recording with "xippmex('trial', 'stopped') does stop
                    % the recording but immediately throws an error that
                    % the name of the file already exists. Can't figure out
                    % a way to not have this happen. Because it does stop
                    % the recording, I'm just catching the error message
                    % and having it continue as if nothing happened.
                    obj.StatusMessage = "Recording stopped.";
                    obj.isRecording = false;
                else
                    obj.StatusMessage = ME.message;
                    
                end

            end

        end

        


    end

